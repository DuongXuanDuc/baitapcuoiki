    private void btnFindGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindGVActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập tên giảng viên cần tìm");
        if (input != null && input.length() > 0) {
            gvList = gVModify.findByTen(input);
            
            tblModel.setRowCount(0);
            
            for (GiangVien giangVien : gvList) {
                tblModel.addRow(new Object[]{tblModel.getRowCount() + 1, giangVien.getHoTen(), giangVien.getGioiTinh(), giangVien.getNgaySinh(), giangVien.getEmail(), giangVien.getKhoa()});
            }
        }else{
            showGV();
        }
    }//GEN-LAST:event_btnFindGVActionPerformed

    private void btnAddGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddGVActionPerformed
        // TODO add your handling code here:
        String hoTen = txtHoTenGV.getText().trim();
        String gioiTinh = cbGioiTinhGV.getSelectedItem().toString();
        String ngaySinh = txtDayGV.getText().trim() + "/" + txtMonthGV.getText().trim() + "/" + txtYearGV.getText().trim();
        String email = txtEmailGV.getText().trim();
        String khoa = cbKhoaGV.getSelectedItem().toString();
        
        GiangVien gv = new GiangVien(hoTen, gioiTinh, ngaySinh, email, khoa);
        
        gVModify.insert(gv);
        
        showGV();
    }//GEN-LAST:event_btnAddGVActionPerformed

