    private void btnDeleteSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSVActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblSinhVien.getSelectedRow();
        if (selectedIndex >= 0) {
            SinhVien sv = svList.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa?");
            System.out.println(option);
            if (option == 0) {
                sVModify.delete(sv);
            }
        }
        showSV();
    }//GEN-LAST:event_btnDeleteSVActionPerformed

    private void btnFindSVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindSVActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập tên sinh viên cần tìm");
        if (input != null && input.length() > 0) {
            svList = sVModify.findByTen(input);
            
            tblModelSV.setRowCount(0);
            
            for (SinhVien sinhVien : svList) {
                tblModelSV.addRow(new Object[]{tblModelSV.getRowCount() + 1, sinhVien.getHoTen(), sinhVien.getGioiTinh(), sinhVien.getNgaySinh(), sinhVien.getEmail(), sinhVien.getKhoa(), sinhVien.getNamNhapHoc()});
            }
        }else{
            showSV();
        }
    }//GEN-LAST:event_btnFindSVActionPerformed

