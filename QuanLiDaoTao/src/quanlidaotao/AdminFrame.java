    private void btnDeleteGVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteGVActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblGiangVien.getSelectedRow();
        if (selectedIndex >= 0) {
            GiangVien gv = gvList.get(selectedIndex);
            
            int option = JOptionPane.showConfirmDialog(this, "Bạn muốn xóa?");
            System.out.println(option);
            if (option == 0) {
                gVModify.delete(gv);
            }
        }
        showGV();
    }//GEN-LAST:event_btnDeleteGVActionPerformed

